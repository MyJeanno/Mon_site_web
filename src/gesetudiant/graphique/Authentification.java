    private void btnconnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconnectActionPerformed
        Connection con = AccessDB.obtenirConnection();
        String sql = "SELECT * FROM user WHERE LOGIN = '"+login.getText()+"' AND PASS = '"+pass.getText()+"' ";
        ResultSet rs;
        try {
            PreparedStatement ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Acces autoris√©e");
                nomUser = rs.getString("LOGIN");
                new Accueil().setVisible(true);
            }else{
               JOptionPane.showMessageDialog(null, "Login ou mot de passe incorrect"); 
            }
        } catch (SQLException ex) {
            Logger.getLogger(Authentification.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnconnectActionPerformed

